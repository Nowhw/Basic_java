public class swapping {
    public static void main(String[] args){
        String x="Light";
        String y="Speed";
        System.out.println("x :"+x);
        System.out.println("y :"+y);
        String temp=null;
        temp=x;
        x=y;
        y=temp;
        System.out.println("x :"+x);
        System.out.println("y :"+y);
    }
}
