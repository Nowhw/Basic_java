public class zero_padding {
    public static void main(String[] args){
        int id1=1;
        int id2=23;
        int id3=345;
        int id4=6789;

        System.out.printf("%04d\n", id1);
        System.out.printf("%04d\n", id2);
        System.out.printf("%04d\n", id3);
        System.out.printf("%04d\n", id4);
    }
}
